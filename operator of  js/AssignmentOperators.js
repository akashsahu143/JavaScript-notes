//Assignment Operators (=).

let x = 10;

//Addition
x= x+2;
console.log(x); // Output: 12

x +=5;
console.log(x); // Output: 17


//Subtraction
let y = 20;

y = y-5;
console.log(y); // Output: 15

y -= 3;
console.log(y); // Output: 12

// Multiplication

let z = 3;

z = z*5;
console.log(z); // Output: 15

z *= 3;
console.log(z); // Output: 45

// Division

let i = 10;

i = i/5;
console.log(i);  // Output: 2

i /= 3;
console.log(i) // Output: 5

// Modulus (remainder)

let j = 10;
j = j % 2;
console.log(j); // Output: 0

j%=2;
console.log(j); // Output: 0